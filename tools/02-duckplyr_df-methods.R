walk(r_status, function(file) {
  patch_path <- gsub("R/(.*)[.]R", "patch/\\1.patch", file)
  if (fs::file_exists(patch_path)) {
    system(paste0("patch -p1 -R < ", patch_path))
  }
  system(paste0("git diff -R -- ", file, " > ", patch_path))
  system(paste0("git checkout -- ", file))
})
